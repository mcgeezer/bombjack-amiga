
SMILEY_ANIM_SPEED:	equ	25

HDL_SMILEY:	
	moveq	#0,d1
	bsr	ANIMATE
	rts
	
SMILEY:		dc.l	ANIM_SMILEY_VARS
		dc.l	HDL_SMILEY
		dc.l	ANIM_SMILEY_ACTIVE
		dc.w	-1

ANIM_SMILEY_VARS:	dc.w	0,80,80,0
			dc.l	ANIM_SMILEY_BUFFER


ANIM_SMILEY_BUFFER:	ds.l	16*(SPR16_BITPLANES*2)



ANIM_SMILEY_ACTIVE:	dc.l	ANIM_SMILEY_ACTIVE_VARS
			dc.l	ANIM_SMILEY_ACTIVE_FRAME
			dc.l	ANIM_SMILEY_ACTIVE_SPEED
			
ANIM_SMILEY_ACTIVE_VARS:	dc.w	0,0
ANIM_SMILEY_ACTIVE_FRAME:	dc.w	79,79,79,79,79,79,119,79,119,79  ;10sec
				dc.w	119,79,119,119,$ffff
ANIM_SMILEY_ACTIVE_SPEED:	dc.w	50







